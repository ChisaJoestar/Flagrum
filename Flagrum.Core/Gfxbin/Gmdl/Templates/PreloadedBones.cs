    public static List<BoneHeader> Get(int type, int target, string variant = null)
    {
        if (type == (int)BinmodType.Cloth)
        {
            var modTarget = (OutfitSoloTarget)target;

            return modTarget switch
            {
                OutfitSoloTarget.Noctis => NoctisData.PreloadedBones,
                OutfitSoloTarget.Prompto => PromptoData.PreloadedBones,
                OutfitSoloTarget.Ignis => IgnisData.PreloadedBones,
                OutfitSoloTarget.Gladiolus => GladiolusData.PreloadedBones,
                _ => new List<BoneHeader>()
            };
        }

        return new List<BoneHeader>();
    }

